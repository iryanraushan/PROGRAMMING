#include <iostream>
using namespace std;
int main()
{
    int a = 20, b = 5;
    cout << " the value of a+b is: " << a + b;
    cout << " \nthe value of a-b is: " << a - b;
    cout << " \nthe value of a*b is: " << a * b;
    cout << " \nthe value of a/b is: " << a / b;
    cout << " \nthe value of a%b is: " << a % b;
    cout << " \nthe value of a++ is: " << a++; // a=21.
    cout << " \nthe value of ++a is: " << ++a; // a=22.
    cout << " \nthe value of --a is: " << --a; // a=21.
    cout << " \nthe value of ++a is: " << ++a; // a=22.
    cout << " \nthe value of --a is: " << --a; // a=21
    return 0;
}