// C++ Program to Find Size of int, float, double and char.

#include <iostream>

using namespace std;

int main () {
    
    cout << "Size of int :\t" << sizeof(int) << " bytes" <<endl;
    cout << "Size of float :\t" << sizeof(float) << " bytes" <<endl;
    cout << "Size of double :\t" << sizeof(double) << " bytes" <<endl;
    cout << "Size of char :\t" << sizeof(char) << " byte" <<endl;
    
    return 0;
}